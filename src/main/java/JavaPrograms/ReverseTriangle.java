package JavaPrograms;

public class ReverseTriangle {

	public static void main(String[] args) {
		{  
			int rowCount=6;      
			for (int i= rowCount-1; i>=0 ; i--)  
			{  
			for (int j=0; j<=i; j++)  
			{  
			System.out.print("*" + " ");  
			}  
			System.out.println();  
			}  
			}  
			}  

	}


