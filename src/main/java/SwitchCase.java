
public class SwitchCase {

	public static void main(String[] args) {
		int day=12;

		switch(day){

		case 1:
		System.out.println("SUNDAY");
		break;
		case 2:
		System.out.println("MONDAY");
		break;
		case 3:
		System.out.println("TUESDAY");
		break;
		default:
		System.out.println("No days matched");
		break;
		}

	}

}
